<?php

namespace Demo;

class User
{
    private $id;
    private $username;
    private $email;


    public function __construct($id)
    {
        $this->setId($id);
        $this->setUsername('username-' . $this->getId());
        $this->setEmail('user+' . $this->getId() . '@example.com');
    }

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param int $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return string
     */
    public function getUsername()
    {
        return $this->username;
    }

    /**
     * @param string $username
     */
    public function setUsername($username)
    {
        $this->username = $username;
    }

    /**
     * @return string
     */
    public function getEmail()
    {
        return $this->email;
    }

    /**
     * @param string $email
     */
    public function setEmail($email)
    {
        $this->email = $email;
    }

}